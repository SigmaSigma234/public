num1 = int(input("Please enter your first number: "))
num2 = int(input("Please enter your first number: "))
op = input("What operator would you like to use, +, -, * or /: ")
if op == "+":
    print(num1,"+",num2,"=",num1+num2)
if op == "-":
    print(num1,"-",num2,"=",num1-num2)
if op == "*":
    print(num1,"*",num2,"=",num1*num2)
if op == "/":
    print(num1,"/",num2,"=",num1/num2)

